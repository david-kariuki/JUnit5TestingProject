package classes;

public class Calculator {

    /**
     * Method to perform addition
     * @param a - int
     * @param b - int
     * @return int
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * Method to perform division
     * @param a - int
     * @param b - int
     * @return int
     */
    public int divide(int a, int b) {
        return a / b;
    }
}
